package ccs.framework.model;

public class UserPermission {

	private String AUTHOR ;
	 
	 
	private String AUTHOR_NM ;
	 
	 
	private String MAIN_PAGE;
	 
	public String getAUTHOR() {
		return AUTHOR;
	}


	public void setAUTHOR(String AUTHOR) {
		this.AUTHOR = AUTHOR;
	}
	
	public String getAUTHOR_NM() {
		return AUTHOR_NM;
	}


	public void setAUTHOR_NM(String AUTHOR_NM) {
		this.AUTHOR_NM = AUTHOR_NM;
	}
	
	public String getMAIN_PAGE() {
		return MAIN_PAGE;
	}


	public void setMAIN_PAGE(String mAIN_PAGE) {
		MAIN_PAGE = mAIN_PAGE;
	}

}
